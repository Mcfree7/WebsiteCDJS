        <p>La Décision N° A/DEC 13/01/2005 du 19/01/2005 du 19 janvier 2005 avait adopté l’organigramme du CDJS/CEDEAO, composé de quatre Divisions (Jeunesse, Sports, Administration et Finance, et TIC).
